    private void buttonTambahPCActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonTambahPCActionPerformed
        if (txtNoPC.getText().equals("")){
            JOptionPane.showMessageDialog(null,"No PC Belum diisi");
            txtNoPC.requestFocus();
        }else if (txtMerekPC.getText().equals("")){
            JOptionPane.showMessageDialog(null,"Merek PC Belum diisi");
            txtMerekPC.requestFocus();
        }else if (txtJenisPC.getText().equals("")){
            JOptionPane.showMessageDialog(null,"Jenis PC Belum diisi");
            txtJenisPC.requestFocus();
        }else if(txtHargaPC.getText().equals("")){
           JOptionPane.showMessageDialog(null,"Harga sewa perhari PC Belum diisi");
            txtHargaPC.requestFocus();  
        }else if (txtStokPC.getText().equals("")){
             JOptionPane.showMessageDialog(null,"Stok PC Belum diisi");
             txtStokPC.requestFocus();
        } else if (new configDB().getDuplikasiKey("pc","no_pc",txtNoPC.getText())){
            JOptionPane.showMessageDialog(null,"No PC sudah terdaftar");
        }else{
           // area koding simpan 
           
           String[] FPC = {"no_pc","merek","jenis", "Harga","Stok"};
           String[] isiPC ={txtNoPC.getText(),txtMerekPC.getText(),txtJenisPC.getText(),txtHargaPC.getText(),txtStokPC.getText()};
           new configDB().SimpanDinamis("pc", FPC, isiPC);
           new configDB().setJudulKolom(tabelPC, JudulPC);
           new configDB().setTampilTabel(tabelPC, JudulPC, SQLPC);
           new configDB().setLebarJudulKolom(tabelPC, LebarKolomPC);
           
            txtNoPC.setText("");
            txtMerekPC.setText("");
            txtJenisPC.setText("");
            txtHargaPC.setText("");
            txtStokPC.setText("");
            txtNoPC.requestFocus();

            txtNoPC.enable(true);
        }
    }//GEN-LAST:event_buttonTambahPCActionPerformed

